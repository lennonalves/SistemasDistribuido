    private void btnLigarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLigarActionPerformed
        // TODO add your handling code here:
        lblStatus.setText("Status: Servidor Ativo");
        try {
            DatagramSocket s = new DatagramSocket(Integer.parseInt(txtPorta.getText()));
            byte[] buffer = new byte[1000];
            while (true)
            {
                DatagramPacket request = new DatagramPacket(buffer, buffer.length);
                s.receive(request);
                
                String m = new String(request.getData());
                m = m.toUpperCase();
                byte[] resposta = m.getBytes();
                
                DatagramPacket reply = new DatagramPacket(resposta,
                request.getLength(), request.getAddress(), request.getPort());
                s.send(reply);
            }
        }
        catch (IOException e) 
        {
            lblStatus.setText("Status: Desconectado");
            System.out.println("IOException: " + e);
        }
    }//GEN-LAST:event_btnLigarActionPerformed

